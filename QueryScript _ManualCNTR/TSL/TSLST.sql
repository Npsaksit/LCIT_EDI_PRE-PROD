SELECT TTCL.CNTR_OPER_CODE,
TTCL.CNTR_NUM AS CNTR_AN,
'No Vessel' VESSEL_NM_AN,
 decode(TTCL.CNTR_TYPE,'2210','20GP','2200','20GP','2250','20GP','22G1','20GP',
 '2230','20RF','2251','20OT','2263','20FR','22T6','20TK','2500','20HQ','4300','40GP',
 '4310','40GP','4351','40OT','4250','40OT','4363','40FR','4364','40FR','4351','40OT',
 '4500','40HQ','4530','40RH','4532','40RH','45R0','40RH','45R1','40RH','4550','45OT','45G1','40HQ',TTCL.CNTR_TYPE) AS CONTAINER_TYPE_CODE,
 TO_CHAR(TTCL.CNTR_ARRIVE_TM,'DD-MM-YYYY HH24:MI') AS ACTIVITY_TM,
 'DV' STATUS,
 DECODE((SELECT C.AREA_C FROM TMS_CNTR_GRP1 C WHERE c.CNTR_AN=TTCL.CNTR_NUM),'B5','LCNO5','C3','LCNC3') TERMIANL,
 'THLCB' PORT,
 'COC' OWNER_STATUS
 FROM TMS_OWNER.TMV_CNTR_CYCLE_LIST TTCL  
 WHERE TTCL.CNTR_ARRIVE_TM  between  to_date(':START_DATE','DD-MON-YYYY HH24:MI') AND to_date(':END_DATE','DD-MON-YYYY HH24:MI')
 AND TTCL.SHIPPING_STATUS_CODE LIKE 'ST'
 AND TTCL.CNTR_OPER_CODE = 'TSL'
 AND (SELECT C.AREA_C FROM TMS_CNTR_GRP1 C WHERE c.CNTR_AN=TTCL.CNTR_NUM) LIKE :BERTH 
 AND TTCL.CNTR_NUM IN (:CNTR) 